def make_tensor_to_np_image(tensor):
    # tensor:  B,C,H,W
    # pytorch tensor -> [-1,1] -> [0,255] np array
    return (
        tensor.detach()
        .clamp_(min=-1, max=1)
        .add(1)
        .div_(2)
        .mul(255)
        .type(torch.uint8)
        .permute(0, 2, 3, 1)
        .to("cpu")
        .numpy()
    )


